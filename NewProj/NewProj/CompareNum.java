package NewProj;

public class CompareNum {
	public static void main (String args[]) {
		
	
		int num1 = 25;
		int num2 = 39;
		
		if(num1 != num2) {
			System.out.println(num1 +" != " + num2);
		}
		else {
			System.out.println(num1 +" == " + num2);

		}
		if(num1 <= num2) {
			System.out.println(num1 +" <= " + num2);
			
		}
		if(num1 < num2) {
			System.out.println(num1 +" < " + num2);
			
		}

}
}