package NewProj;

public class BitWiseOper {
	
	  public static void main(String[] args) {
	        int x = 9;
	        int y = 8;

	        int andResult = x & y;  
	        int orResult = x | y;   
	        int xorResult = x ^ y; 

	        
	        System.out.println("x & y = " + andResult);
	        System.out.println("x | y = " + orResult);
	        System.out.println("x ^ y = " + xorResult);
	    }

}
